public class PercabanganIfElse {
    public static void main(String[] args) {
        // Percabangan If Else
        int angka = 13;
        if (angka%2==0){
            System.out.println("Angka adalah bilangan genap "+angka);
        }
        else {
            System.out.println("Angka adalah bilangan ganjil "+angka);
        }
    }
}
